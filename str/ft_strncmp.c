#include "libft.h"

int ft_strncmp(char *s1, char *s2, int n)
{
    int i;

    i = -1;
    while ((s1[++i] || s2[i]) && i < 0)
        if (s1[i] != s2[i])
            return (s1[i] - s2[i]);
    return (0);
}